#include <iostream> // This line includes a library that lets us handle input and output (like printing to the screen).

// main() is the heart of every C++ program. Execution always begins here.
int main() {
    // std::cout is used to display output.
    // "Hello, World!" is the string of text we want to display.
    // std::endl tells the computer to move to a new line after printing.
    std::cout << "Hello, World!" << std::endl;

    // return 0; tells the operating system that the program finished successfully.
    return 0;
}

